					</ol>
					<p>
						<br />2. RECHT AUF BERICHTIGUNG<br />Sie haben ein Recht auf Berichtigung und/oder
						Vervollständigung gegenüber dem Verantwortlichen, sofern die verarbeiteten
						personenbezogenen Daten, die Sie betreffen, unrichtig oder unvollständig sind. Der
						Verantwortliche hat die Berichtigung unverzüglich vorzunehmen.<br />3. Recht auf
						Einschränkung der Verarbeitung<br />Unter den folgenden Voraussetzungen können Sie die
						Einschränkung der Verarbeitung der Sie betreffenden personenbezogenen Daten verlangen:

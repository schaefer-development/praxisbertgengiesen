						</li>
					</ol>
					<p><br />4. RECHT AUF LÖSCHUNG</p>
					<p>
						a) Löschungspflicht<br />Sie können von dem Verantwortlichen verlangen, dass die Sie
						betreffenden personenbezogenen Daten unverzüglich gelöscht werden, und der
						Verantwortliche ist verpflichtet, diese Daten unverzüglich zu löschen, sofern einer der
